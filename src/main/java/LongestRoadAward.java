public class LongestRoadAward {
    private Player player;
    private int amountOfRoads;

    LongestRoadAward(){}

    public void setPlayer(Player player) {
        this.player = player;
    }

    public void setAmountOfRoads(int amountOfRoads) {
        this.amountOfRoads = amountOfRoads;
    }

    public Player getPlayer() {
        return player;
    }

    public int getAmountOfRoads() {
        return amountOfRoads;
    }
}
